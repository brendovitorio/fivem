function ToggleActionMenu()
	menuactive = not menuactive
    if menuactive then
		SetNuiFocus(true,true)
		SendNUIMessage({ action = "open", title = "Rotas para "..routes[currentRoute].title, items = vSERVER.getItems(currentRoute) })
